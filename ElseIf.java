public class ElseIf {
    public static void main(String args[]) {
        int number = 10;
        if(number > 0) {
            System.out.println("Number is Positive");
        } else if (number < 0){
            System.out.println("Number is Not Positive");
        } else {
            System.out.println("Number is 0");

        }
    }
}