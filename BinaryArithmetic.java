public class BinaryArithmetic {
    public static void main(String args[]) {
        int i = 20, j = 10, result;
        result = i + j;    // addition
        System.out.println(result);
        result = i - j;    // subtraction
        System.out.println(result);
        result = i * j;   // multiplication
        System.out.println(result);
        result = i / j;   // division
        System.out.println(result);
        result = i % j;   // modulus
        System.out.println(result);
    }
}