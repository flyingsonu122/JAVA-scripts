public class RelationalOperators {
    public static void main(String args[]) {
        int i = 20, j = 10;
        System.out.println("Result: if i = 20 and j = 10");
        
        
        System.out.println("i > j = " + (i > j));   // greater than
        System.out.println("i < j = " + (i < j));   // less than
        System.out.println("i >= j = " + (i >= j)); // greater than or equal to
        System.out.println("i <= j = " + (i <= j)); // less than or equal to
        System.out.println("i == j = " + (i == j)); // equal to
        System.out.println("i != j = " + (i != j)); // not equal to
    }
}